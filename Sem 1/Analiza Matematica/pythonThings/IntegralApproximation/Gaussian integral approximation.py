"""
Remarks:
 - increasing the number of steps and/or the range increases the precision
 - for a wider range you need more steps to achieve the same precision
 - the majority of the contribution comes from around 0, so you need a relatively small range to get a good approximation(
 for example if the range is [-5;5] the error is 0 due to the small contributions of (-inf;-5) and (5; inf) and floating
 point arithmetic approximation errors)
"""

import math
from texttable import Texttable

def TrapeziumApproximation(n: int, lb: float, ub: float, f) -> float:
    integral = 0
    x = lb
    step = (ub - lb)/n

    integral += f(lb) / 2
    for i in range(1, n):
        x += step
        integral += f(x)
    integral += f(ub) / 2

    integral *= step

    return integral

def f(x: float) -> float:
    return math.exp(-x**2)

tt = Texttable()
tt.header(["a", "n", "I", "Error"])
sqrt_pi = math.sqrt(math.pi)
for a in [0.1, 1, 1.5, 2, 5, 100, 10**6]:
    for n in [1, 10 ** 2, 10 ** 4, 10 ** 6, 10 ** 8]:
        I = TrapeziumApproximation(n, -a, a, f)
        tt.add_row([a, n, I, abs(I - sqrt_pi)])

print(tt.draw())

